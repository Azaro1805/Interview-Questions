
public class QQ17 {

	public static void main(String[] args) {
		int first =100;
		int second =200;
	swapValues(first,second);
	System.out.println(first);
	System.out.println(second);
	}

	public static void swapValues(int first , int second) {
		int temp = first;
		first = second ;
		second = temp;
	}
}
